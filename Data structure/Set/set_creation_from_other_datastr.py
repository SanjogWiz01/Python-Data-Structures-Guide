# set can be declare using list, tuple ,string,dictionary
# set from list
myset = set([1, 2, 3, 4, 5])
print(myset)
# set from tuple
tu=(1, 2, 3, 4, 5)
myset = set(tu)
print(myset)
# set from string
myset = set("hello")
print(myset)
# set from dictionary
mydict = {'a': 1, 'b': 2, 'c': 3}
myset = set(mydict)
