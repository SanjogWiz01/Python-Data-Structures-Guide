# list_functions.py
# Built-in functions on lists

nums = [10, 20, 30, 40, 50]

print("Length:", len(nums))
print("Max:", max(nums))
print("Min:", min(nums))
print("Sum:", sum(nums))
print("Average:", sum(nums) / len(nums))
print("Count of 20:", nums.count(20))
print("Index of 30:", nums.index(30))